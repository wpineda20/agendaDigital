        $places->zone_id = Zone::where('zone_name', $request->zone_name)->first()->id;
        $places->deleted_at = $request->deleted_at;

        $places->save();

        return response()->json([
            "status" => 200,
            "message" => "Registro modificado correctamente.",
            "success" => true,
        ]);
    }

