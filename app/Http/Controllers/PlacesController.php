<?php

namespace App\Http\Controllers;

use App\Models\Place;
use App\Models\Zone;

use Illuminate\Http\Request;
use Encrypt;

class PlacesController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $itemsPerPage = $request->itemsPerPage;
        $skip = ($request->page - 1) * $request->itemsPerPage;

        // Getting all the records
        if (($request->itemsPerPage == -1)) {
            $itemsPerPage =  Place::count();
            $skip = 0;
        }

        $sortBy = (isset($request->sortBy[0])) ? $request->sortBy[0] : 'id';
        $sort = (isset($request->sortDesc[0])) ? "asc" : 'desc';

        $search = (isset($request->search)) ? "%$request->search%" : '%%';

        $places = Place::allDataSearched($search, $sortBy, $sort, $skip, $itemsPerPage);
        $places = Encrypt::encryptObject($places, "id");

        $total = Place::counterPagination($search);

        return response()->json([
            "status" => 200,
            "message" => "Registros obtenidos correctamente.",
            "places" => $places,
            "total" => $total,
            "success" => true,
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $places = new Place;

        $places->place_name = $request->place_name;
        $places->zone_id = Zone::where('zone_name', $request->zone_name)->first()->id;
        $places->deleted_at = $request->deleted_at;

        $places->save();

        return response()->json([
            "status" => 200,
            "message" => "Registro creado correctamente.",
            "success" => true,
        ]);
    }

